package JavaCourse.Basics_1;

public class Main {
// anything within the classes curly braces belongs to the class
// and is contained within

    // main method compiles and runs code
    // code is executed in order
    public static void main(String[] args) {

        // print to the console
        System.out.println("Hello");
        // "\n" make a space
        System.out.println("World\n");
        // "\t" makes a tab
        System.out.println("\t:)");
        // print line with quotes
        System.out.println("\"bob\"");
        // print backsplash
        System.out.println("\\Lisa\\");


    }

}

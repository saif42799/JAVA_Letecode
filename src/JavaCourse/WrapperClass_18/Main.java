package JavaCourse.WrapperClass_18;

public class Main {
    public static void main(String[] args) {

        // wrapper class = provides a way to use primitive data types as reference data types
        //                 reference data types contain useful methods
        //                 can be used with collections (ex ArrayList)

        // primitive       wrapper
        // ---------       -------
        // boolean         Boolean
        // char            Character
        // int             Integer
        // double          Double

        // autoboxing = the automatic conversion that java compiler makes between
        //              the primitive types and their corresponding object wrapper class
        // unboxing = the reverse of autoboxing. Auto matic conversion of wrapper class to primitive

        Boolean a = true;
        Character b = '@';
        Integer c = 123;
        Double d = 3.14;
        String e = "Bro";






    }
}
